#Curso Python #07 - Operadores Aritméticos

n1 = int(input("Digite um valor: "))
n2 = int(input("Digite outro valor: "))
soma = n1 + n2
mult = n1 * n2
div = n1 / n2
div_int = n1 // n2
exp = n1 ** n2
print(f"A soma é {soma},\n a multiplicação é {mult},\n e a divisão é {div:.2f}")
print(f"Divisão inteira é {div_int} e a potência é {exp}")
